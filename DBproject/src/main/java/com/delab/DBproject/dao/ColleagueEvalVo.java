package com.delab.DBproject.dao;

public class ColleagueEvalVo {
    private int eval_num;
    private int project_num;
    private int eval_emp;
    private int evaled_emp;
    private int communi;
    private int contri;
    
	public int getEval_num() {
		return eval_num;
	}
	public void setEval_num(int eval_num) {
		this.eval_num = eval_num;
	}
	public int getProject_num() {
		return project_num;
	}
	public void setProject_num(int project_num) {
		this.project_num = project_num;
	}
	public int getEval_emp() {
		return eval_emp;
	}
	public void setEval_emp(int eval_emp) {
		this.eval_emp = eval_emp;
	}
	public int getEvaled_emp() {
		return evaled_emp;
	}
	public void setEvaled_emp(int evaled_emp) {
		this.evaled_emp = evaled_emp;
	}
	public int getCommuni() {
		return communi;
	}
	public void setCommuni(int communi) {
		this.communi = communi;
	}
	public int getContri() {
		return contri;
	}
	public void setContri(int contri) {
		this.contri = contri;
	}
}