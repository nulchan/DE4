package com.delab.DBproject.dao;

public class ParticipateVo {
    private int emp_no;
    private int project_num;
    private int duty;
    private int partici_beg;
    private int partici_end;
    
	public int getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}
	public int getProject_num() {
		return project_num;
	}
	public void setProject_num(int project_num) {
		this.project_num = project_num;
	}
	public int getDuty() {
		return duty;
	}
	public void setDuty(int duty) {
		this.duty = duty;
	}
	public int getPartici_beg() {
		return partici_beg;
	}
	public void setPartici_beg(int partici_beg) {
		this.partici_beg = partici_beg;
	}
	public int getPartici_end() {
		return partici_end;
	}
	public void setPartici_end(int partici_end) {
		this.partici_end = partici_end;
	}
 
	
    
	
}